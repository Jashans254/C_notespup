//wap to swap two numbers with third variable/number
#include<stdio.h>
int main() {
	int a,b,c;
	a=10;
	b=20;
	printf("before swapping");
	printf("\n1st no.%d",a);
	printf("\n2nd no.%d",b);
	c=a;
	a=b;
	b=c;
	printf("\nafter swapping");
	printf("\n1st no.%d",a);
	printf("\n2nd no.%d",b);
	return(0);
}
	
